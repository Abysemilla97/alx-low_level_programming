#include "3-calc.h"

/**
 * op_add - calc the sum of two ints.
 * @a: the first int.
 * @b: the second int.
 *
 * Return: the result sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calc the difference of two ints.
 * @a: the first int.
 * @b: the second int.
 *
 * Return: the result difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calc the multiple of two ints.
 * @a: the first int.
 * @b: the second int.
 *
 * Return: the result product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calc the division of two ints.
 * @a: the first int.
 * @b: the second int.
 *
 * Return: the result division of a and b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calc the remainder of the division of two ints.
 * @a: the first int.
 * @b: the second int.
 *
 * Return: the result remainder of the division of a and b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

